import fs from 'fs'
import path from 'path'

const PREAMBLE_START = '<!-- RELAY_PREAMBLE_START - DO NOT EDIT -->'
const PREAMBLE_END = '<!-- RELAY_PREAMBLE_END -->'

/**
 * bin/relay-preamble.sh 스크립트를 생성한다.
 * relay CLI가 있으면 사용, 없으면 curl fallback.
 */
export function generatePreambleScript(slug: string, apiUrl: string): string {
  // slug format: @owner/agent → extract agent slug for ping
  const stripped = slug.startsWith('@') ? slug.slice(1) : slug
  const slashIdx = stripped.indexOf('/')
  const agentSlug = slashIdx !== -1 ? stripped.slice(slashIdx + 1) : stripped
  return `#!/usr/bin/env bash
# relay-preamble.sh — auto-generated by relay publish
set +e

# Device hash (shasum → sha256sum → openssl fallback)
_RAW="$(hostname):$(whoami)"
if command -v shasum &>/dev/null; then
  DEVICE_HASH=$(printf '%s' "$_RAW" | shasum -a 256 | cut -d' ' -f1)
elif command -v sha256sum &>/dev/null; then
  DEVICE_HASH=$(printf '%s' "$_RAW" | sha256sum | cut -d' ' -f1)
elif command -v openssl &>/dev/null; then
  DEVICE_HASH=$(printf '%s' "$_RAW" | openssl dgst -sha256 | awk '{print $NF}')
else
  DEVICE_HASH="unknown"
fi

# Read relay token (for user identification in usage ping)
_RELAY_TOKEN=""
if [ -f "$HOME/.relay/token.json" ]; then
  _RELAY_TOKEN=$(grep -o '"access_token":"[^"]*"' "$HOME/.relay/token.json" 2>/dev/null | head -1 | cut -d'"' -f4)
fi

# Usage ping (with user identity if logged in)
if command -v relay &>/dev/null; then
  relay ping "${slug}" --quiet 2>/dev/null &
elif command -v curl &>/dev/null; then
  _AUTH_HEADER=""
  [ -n "$_RELAY_TOKEN" ] && _AUTH_HEADER="-H \\"Authorization: Bearer $_RELAY_TOKEN\\""
  eval curl -sf --max-time 5 -X POST "${apiUrl}/api/agents/${agentSlug}/ping" \\
    -H "Content-Type: application/json" \\
    $_AUTH_HEADER \\
    -d "{\\"device_hash\\":\\"$DEVICE_HASH\\",\\"slug\\":\\"${slug}\\"}" \\
    2>/dev/null &
fi

# Update check (already 24h cached by relay CLI)
if command -v relay &>/dev/null; then
  _UPD=$(relay check-update "${slug}" --quiet 2>/dev/null || true)
  [ -n "$_UPD" ] && echo "$_UPD" || true
fi

echo "RELAY_READY: ${slug}"
`
}

/**
 * SKILL.md / command에 삽입할 preamble 마크다운.
 * agentDir: 설치된 에이전트의 절대 경로 (install 시점에 결정)
 */
export function generatePreamble(slug: string, agentDir: string): string {
  const scriptPath = path.join(agentDir, 'bin', 'relay-preamble.sh')
  return `${PREAMBLE_START}
## Preamble (run first)

\`\`\`bash
[ -f "${scriptPath}" ] && bash "${scriptPath}" 2>/dev/null || true
\`\`\`

If the bash command above fails or the file is not found (sandbox/MCP environment), use the \`relay_check_update\` MCP tool instead: \`relay_check_update(slug: "${slug}")\`

If output shows \`CLI_UPGRADE_AVAILABLE <old> <new>\`: tell user "relay v{new} available. Run: npm update -g relayax-cli"
If output shows \`AGENT_UPGRADE_AVAILABLE\`: tell user "${slug} v{new} available (by @{author}). Run: relay update ${slug}"
${PREAMBLE_END}`
}

/**
 * agentDir에 bin/relay-preamble.sh를 생성한다.
 */
export function generatePreambleBin(agentDir: string, slug: string, apiUrl: string): void {
  const binDir = path.join(agentDir, 'bin')
  if (!fs.existsSync(binDir)) {
    fs.mkdirSync(binDir, { recursive: true })
  }
  const scriptPath = path.join(binDir, 'relay-preamble.sh')
  fs.writeFileSync(scriptPath, generatePreambleScript(slug, apiUrl))
  fs.chmodSync(scriptPath, 0o755)
}

/**
 * frontmatter(---...---) 뒤에 preamble을 삽입한다.
 */

export function injectPreamble(filePath: string, slug: string, agentDir: string): void {
  const content = fs.readFileSync(filePath, 'utf-8')
  const preamble = generatePreamble(slug, agentDir)

  // 기존 preamble 제거
  let cleaned = content
  const startIdx = cleaned.indexOf(PREAMBLE_START)
  const endIdx = cleaned.indexOf(PREAMBLE_END)
  if (startIdx !== -1 && endIdx !== -1) {
    cleaned =
      cleaned.slice(0, startIdx).trimEnd() +
      '\n' +
      cleaned.slice(endIdx + PREAMBLE_END.length).trimStart()
  }

  // frontmatter 뒤에 삽입
  const fmMatch = cleaned.match(/^---\n[\s\S]*?\n---\n/)
  if (fmMatch) {
    const fmEnd = fmMatch[0].length
    cleaned =
      cleaned.slice(0, fmEnd) +
      '\n' + preamble + '\n\n' +
      cleaned.slice(fmEnd).trimStart()
  } else {
    cleaned = preamble + '\n\n' + cleaned.trimStart()
  }

  fs.writeFileSync(filePath, cleaned)
}

/**
 * 에이전트의 사용자 진입점 파일에 preamble을 주입한다.
 */
export function injectPreambleToAgent(agentDir: string, slug: string): number {
  let count = 0

  // 1. user-invocable 서브 스킬 SKILL.md
  const skillsDir = path.join(agentDir, 'skills')
  if (fs.existsSync(skillsDir)) {
    function walkSkills(dir: string) {
      for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
        const fullPath = path.join(dir, entry.name)
        if (entry.isDirectory()) {
          walkSkills(fullPath)
        } else if (entry.name === 'SKILL.md') {
          const content = fs.readFileSync(fullPath, 'utf-8')
          if (/user-invocable:\s*true/i.test(content)) {
            injectPreamble(fullPath, slug, agentDir)
            count++
          }
        }
      }
    }
    walkSkills(skillsDir)
  }

  // 2. commands/*.md
  const commandsDir = path.join(agentDir, 'commands')
  if (fs.existsSync(commandsDir)) {
    for (const entry of fs.readdirSync(commandsDir, { withFileTypes: true })) {
      if (!entry.isFile() || !entry.name.endsWith('.md')) continue
      injectPreamble(path.join(commandsDir, entry.name), slug, agentDir)
      count++
    }
  }

  return count
}
